"""Генерация штрихкодов EAN-13 и Code128 в виде последовательности модулей.

Возвращаемый формат: список ширин (в модулях), начиная с тёмной полосы:
[bar, space, bar, space, ...]. Рендеринг в картинку - в label.py.
"""

# --- EAN-13 -----------------------------------------------------------------
_L = ["0001101", "0011001", "0010011", "0111101", "0100011",
      "0110001", "0101111", "0111011", "0110111", "0001011"]
_G = [c[::-1].translate(str.maketrans("01", "10")) for c in _L]  # G = реверс R
_R = [c.translate(str.maketrans("01", "10")) for c in _L]
_PARITY = ["LLLLLL", "LLGLGG", "LLGGLG", "LLGGGL", "LGLLGG",
           "LGGLLG", "LGGGLL", "LGLGLG", "LGLGGL", "LGGLGL"]


def ean13_checksum(digits12: str) -> str:
    s = sum(int(d) * (3 if i % 2 else 1) for i, d in enumerate(digits12))
    return str((10 - s % 10) % 10)


def ean13_modules(digits: str):
    """digits: 12 или 13 цифр. Возвращает (полный_код_13, строка модулей '0'/'1')."""
    digits = "".join(ch for ch in digits if ch.isdigit())
    if len(digits) == 12:
        digits += ean13_checksum(digits)
    if len(digits) != 13 or digits[12] != ean13_checksum(digits[:12]):
        raise ValueError("EAN-13: нужно 12 цифр (или 13 с верной контрольной)")
    parity = _PARITY[int(digits[0])]
    bits = "101"
    for i, d in enumerate(digits[1:7]):
        bits += (_L if parity[i] == "L" else _G)[int(d)]
    bits += "01010"
    for d in digits[7:]:
        bits += _R[int(d)]
    bits += "101"
    return digits, bits


# --- Code128 (набор B) -------------------------------------------------------
